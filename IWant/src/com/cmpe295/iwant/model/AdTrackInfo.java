package com.cmpe295.iwant.model;

public class AdTrackInfo {
	private long clicks;
	private long clickthroughs;
	private long impressions;

	public long getClicks() {
		return clicks;
	}

	public void setClicks(long clicks) {
		this.clicks = clicks;
	}

	public long getClickthroughs() {
		return clickthroughs;
	}

	public void setClickthroughs(long clickthroughs) {
		this.clickthroughs = clickthroughs;
	}

	public long getImpressions() {
		return impressions;
	}

	public void setImpressions(long impressions) {
		this.impressions = impressions;
	}
}
